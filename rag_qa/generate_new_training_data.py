def generate_dataset():
    """生成平衡的数据集"""
    data = []
    
    for query in generic_knowledge:
        data.append({"query": query, "label": "通用知识"})
    
    for query in professional_knowledge:
        data.append({"query": query, "label": "专业咨询"})
    
    # 复制数据直到达到目标数量
    target_count = 5000
    while len(data) < target_count:
        data.extend(data[:min(target_count - len(data), len(data))])
    
    random.seed(42)
    random.shuffle(data)
    
    return data


def save_dataset(data, output_file):
    """保存数据集"""
    with open(output_file, 'w', encoding='utf-8') as f:
        for item in data:
            f.write(json.dumps(item, ensure_ascii=False) + '\n')
    
    print(f"数据集已保存至：{output_file}")
    print(f"总样本数：{len(data)}")
    
    generic_count = sum(1 for item in data if item['label'] == '通用知识')
    professional_count = sum(1 for item in data if item['label'] == '专业咨询')
    print(f"通用知识：{generic_count} 条")
    print(f"专业咨询：{professional_count} 条")
    print(f"类别比例：{generic_count}:{professional_count}")
    
    print(f"\n前 10 条样本预览:")
    for i, item in enumerate(data[:10], 1):
        label_display = "通用" if item['label'] == '通用知识' else "专业"
        print(f"{i}. [{label_display}] {item['query'][:50]}...")


if __name__ == '__main__':
    print("=" * 60)
    print("开始生成高质量采矿领域分类训练数据集")
    print("=" * 60)
    
    dataset = generate_dataset()
    output_path = 'classify_data/training_dataset_mining_5000.json'
    save_dataset(dataset, output_path)
    
    print("\n" + "=" * 60)
    print("数据集生成完成！")
    print("=" * 60)
